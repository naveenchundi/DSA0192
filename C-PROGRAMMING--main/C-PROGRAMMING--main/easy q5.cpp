#include <iostream>
using namespace std;

int main() {
    // Declare and initialize two integer variables
    int num1 = 5;
    int num2 = 10;

    // Perform addition using the + operator
    int sum = num1 + num2;

    // Print the result of addition
    cout << "Sum of " << num1 << " and " << num2 << " is: " << sum << endl;

    return 0;
}

