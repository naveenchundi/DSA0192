#include <iostream>
using namespace std;

int main() {
    // Integer variable
    int myInt = 10;
    cout << "Integer variable: " << myInt << endl;

    // Float variable
    float myFloat = 3.14;
    cout << "Float variable: " << myFloat << endl;

    // Character variable
    char myChar = 'A';
    cout << "Character variable: " << myChar << endl;

    return 0;
}

