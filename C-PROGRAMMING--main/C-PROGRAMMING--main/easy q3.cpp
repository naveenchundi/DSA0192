#include <iostream>
using namespace std;

int main() {
    // Declare an integer variable
    int myInt = 5;

    // Declare a float variable
    float myFloat;

    // Assign the integer variable to the float variable
    myFloat = myInt;

    // Print the values of both variables
    cout << "Integer variable: " << myInt << endl;
    cout << "Float variable after assignment: " << myFloat << endl;

    return 0;
}

